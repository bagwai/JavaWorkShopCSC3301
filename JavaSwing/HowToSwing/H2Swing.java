		//++++++++++++++++++++++++++++++++										//++
		//++++++++++++++++++++++++++++++++										//++
																				//++
		//++++++++++++++++++++++												//++
		//+++++ Step 5 +++++++++												//++
		panel.add(lblA);	//++												//++
